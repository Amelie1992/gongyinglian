package com.wzlue.chain.goods.entity.apply;

import java.math.BigDecimal;
/**
 * 商品申请产地信息表(商品申请产地信息)
 *
 * @date 2018-08-13
 */
public class ItemPlaceApplyEntity {
    private static final long serialVersionUID = 1L;

    //主键自增
    private Long id;
    //地区
    private String place;
    //商品申请id
    private Long goodApplyId;
    //数值
    private BigDecimal numerical;
    //关税
    private BigDecimal tariff;
    //增值税
    private BigDecimal valueAddTariff;
    //附加税
    private BigDecimal additionDuty;

    private String placeName;

    private String remark;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public Long getGoodApplyId() {
        return goodApplyId;
    }

    public void setGoodApplyId(Long goodApplyId) {
        this.goodApplyId = goodApplyId;
    }

    public BigDecimal getNumerical() {
        return numerical;
    }

    public void setNumerical(BigDecimal numerical) {
        this.numerical = numerical;
    }

    public BigDecimal getTariff() {
        return tariff;
    }

    public void setTariff(BigDecimal tariff) {
        this.tariff = tariff;
    }

    public BigDecimal getValueAddTariff() {
        return valueAddTariff;
    }

    public void setValueAddTariff(BigDecimal valueAddTariff) {
        this.valueAddTariff = valueAddTariff;
    }

    public BigDecimal getAdditionDuty() {
        return additionDuty;
    }

    public void setAdditionDuty(BigDecimal additionDuty) {
        this.additionDuty = additionDuty;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

}
