package com.wzlue.chain.sys.entity;

import java.io.Serializable;
import java.util.Date;


/**
 * 推送消息
 * 
 * @author hjw
 * @email love@und.win
 * @date 2018-08-02 09:24:54
 */
public class SysPushMessageEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	
	//id
	private Long id;
	//员工id
	private String userId;
	//消息类型(0:系统通知，1：交易助手）
	private Integer type;
	//推送消息内容
	private String message;
	//url
	private String url;
	//推送状态（0：未推送；1：推送成功）
	private Integer flag;
	//创建时间
	private Date createDate;
	//更新时间
	private Date updateDate;
	//推送内容
	private String content;
	//阅读标志（0：未，1：已）
	private Integer readerFlag;

	/**
	 * 设置：id
	 */
	public void setId(Long id) {
		this.id = id;
	}
	/**
	 * 获取：id
	 */
	public Long getId() {
		return id;
	}
	/**
	 * 设置：员工id
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}
	/**
	 * 获取：员工id
	 */
	public String getUserId() {
		return userId;
	}
	/**
	 * 设置：消息类型(0:系统通知，1：交易助手）
	 */
	public void setType(Integer type) {
		this.type = type;
	}
	/**
	 * 获取：消息类型(0:系统通知，1：交易助手）
	 */
	public Integer getType() {
		return type;
	}
	/**
	 * 设置：推送消息内容
	 */
	public void setMessage(String message) {
		this.message = message;
	}
	/**
	 * 获取：推送消息内容
	 */
	public String getMessage() {
		return message;
	}
	/**
	 * 设置：url
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * 获取：url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * 设置：推送状态（0：未推送；1：推送成功）
	 */
	public void setFlag(Integer flag) {
		this.flag = flag;
	}
	/**
	 * 获取：推送状态（0：未推送；1：推送成功）
	 */
	public Integer getFlag() {
		return flag;
	}
	/**
	 * 设置：创建时间
	 */
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	/**
	 * 获取：创建时间
	 */
	public Date getCreateDate() {
		return createDate;
	}
	/**
	 * 设置：更新时间
	 */
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}
	/**
	 * 获取：更新时间
	 */
	public Date getUpdateDate() {
		return updateDate;
	}
	/**
	 * 设置：推送内容
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * 获取：推送内容
	 */
	public String getContent() {
		return content;
	}
	/**
	 * 设置：阅读标志（0：未，1：已）
	 */
	public void setReaderFlag(Integer readerFlag) {
		this.readerFlag = readerFlag;
	}
	/**
	 * 获取：阅读标志（0：未，1：已）
	 */
	public Integer getReaderFlag() {
		return readerFlag;
	}
}
