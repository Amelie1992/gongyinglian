package com.wzlue.chain.common.utils;

public class SplitUtil {

    /**
     * 由文件路径截取文件名
     *
     * @param picUrl 文件路径
     * @return 返回文件名
     */
    public static String splitPicName(String picUrl) {
        String picName = picUrl.substring(picUrl.length()-15);
        return picName;
    }
}
